class conditional 
{
    public static void main(String[] args)
    {
        int age = 50;
        
        if (age>=60){
            System.out.println("You are an old person");
        }
        else if(age>=18 )
        {
            System.out.println("You are an adult");
        }
        else{
            System.out.println("You are a child");
        }
        
    }
}